// most used maths functions

console.log(Math.round(10.5)) // round to nearest integer
console.log(Math.ceil(10.1)) // round up
console.log(Math.floor(10.9)) // round down
console.log(Math.trunc(10.9)) // truncate decimal part
console.log(Math.pow(2, 3)) // power of a number (2^3 = 8)
console.log(Math.sqrt(16)) // square root of a number
console.log(Math.abs(-10)) // absolute value
console.log(Math.max(10, 20, 30)) // maximum value
console.log(Math.min(10, 20, 30)) // minimum value
console.log(Math.random()) // random number between 0 and 1
console.log(Math.random() * 100) // random number between 0 and 100
console.log(Math.floor(Math.random() * 100)) // random integer between 0 and 99
console.log(Math.PI) // value of pi
console.log(Math.E) // value of e (Euler's number)
console.log(Math.log(10)) // natural logarithm of a number
console.log(Math.log10(100)) // base 10 logarithm of a number


